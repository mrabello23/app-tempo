    public function buscaApi($idCidadeApi, $limitaResultado = false)
    {
        $url = env('API_ENDPOINT') . '?';
        $url.= 'APPID=' . env('API_APPID');
        $url.= '&lang=' . env('API_LANG');
        $url.= '&units=' . env('API_FORMAT');
        $url.= '&id=' . $idCidadeApi;

        if ($limitaResultado) {
            $url.= '&cnt=' . $limitaResultado;
        }

        $ch = curl_init($url);
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
        $retornoApi = curl_exec($ch);
        curl_close($ch);

        return $retornoApi;
    }
