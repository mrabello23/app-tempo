    public function validaCidade($nome, $arquivo = null)
    {
        $nome = $this->normalizaString($nome);

        if (!$arquivo) {
            $arquivo = "../public/city.list.json";
        }

        $jsonDados = file_get_contents($arquivo);
        $arrayDados = json_decode($jsonDados, true);

        foreach ($arrayDados as $value) {
            if ($nome == $this->normalizaString($value['name'])) {
                return $value;
            }
        }

        throw new \Exception('Cidade nao localizada na API');
    }

